SET SERVEROUTPUT ON

DECLARE

A NUMBER;
B BOOLEAN;
X EXCEPTION;

BEGIN

A:='&U';

IF(A<0) THEN
RAISE X;
END IF;

B:=PRIME(A);

IF(B=FALSE) THEN
DBMS_OUTPUT.PUT_LINE(A||' IS A PRIME NUMBER');
ELSE
DBMS_OUTPUT.PUT_LINE(A||' IS NOT A PRIME NUMBER');
END IF;

EXCEPTION
WHEN X THEN
DBMS_OUTPUT.PUT_LINE('INVALID INPUT');

END;
/

