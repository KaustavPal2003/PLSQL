CREATE OR REPLACE FUNCTION EMP1(E IN EMP.EMPNO%TYPE) RETURN NUMBER IS

S EMP.SAL%TYPE;
C EMP.COMM%TYPE;
TOTAL NUMBER;

BEGIN

SELECT SAL,COMM INTO S,C FROM EMP WHERE EMPNO=E;

IF C IS NULL THEN
RETURN S;
END IF;

TOTAL:=S+C;

RETURN TOTAL;

EXCEPTION 
WHEN NO_DATA_FOUND
THEN DBMS_OUTPUT.PUT_LINE('INVALID EMPNO');

RETURN -1;

END;
/