CREATE OR REPLACE FUNCTION GRADE1(I IN ITEM.INO%TYPE) RETURN NUMBER IS

R ITEM.RATE%TYPE;
NET NUMBER;

BEGIN

SELECT RATE INTO R FROM ITEM WHERE INO=I;

NET:=R;

NET:=NET+NET*(.20);
NET:=NET+NET*(.18);

RETURN NET;

EXCEPTION 
WHEN NO_DATA_FOUND
THEN DBMS_OUTPUT.PUT_LINE('INVALID INO');

RETURN -1;

END;
/