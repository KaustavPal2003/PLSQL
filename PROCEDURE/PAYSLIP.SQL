CREATE OR REPLACE PROCEDURE PAY IS

K PAYSLIP1.DA%TYPE;
J PAYSLIP1.HRA%TYPE;
L PAYSLIP1.PF%TYPE;
C PAYSLIP1.NETSAL%TYPE;

RD RATE.RDA%TYPE;
RH RATE.RHRA%TYPE;
RP RATE.RPF%TYPE;

CURSOR Q IS  SELECT EMPID,BASIC FROM EMPLOYEE;

BEGIN

SELECT RDA,RHRA,RPF INTO RD,RH,RP FROM RATE;

dbms_output.put_line('EMPID'||'	'||'DA'||'	'||'HRA'||'	'||'PF'||'	'||'NETSAL');

FOR I IN Q

LOOP

K:=I.BASIC*RD;
J:=I.BASIC*RH;
L:=I.BASIC*RP;
C:=I.BASIC+K+J+L;

EXIT WHEN Q%NOTFOUND;

dbms_output.put_line(I.EMPID||'	'||K||'	'||J||'	'||L||'	'||C);

INSERT INTO PAYSLIP1 VALUES(I.EMPID,K,J,L,C);

END LOOP;

END;
