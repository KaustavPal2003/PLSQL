SET SERVEROUTPUT ON

DECLARE
ENO EMP.EMPNO%TYPE;
NM EMP.ENAME%TYPE;
SA NUMBER;
TAX NUMBER;
NET NUMBER;

BEGIN
SELECT EMPNO,ENAME,SAL INTO ENO,NM,SA FROM EMP WHERE EMPNO='&A';

IF(SA<=2000) THEN
TAX:=0;
ELSIF(SA<=4000)THEN
TAX:= .02*SA;
ELSE
TAX:=.04*SA;
END IF;
NET:=SA-TAX;

DBMS_OUTPUT.PUT_LINE('EMPNO='||ENO);
DBMS_OUTPUT.PUT_LINE('ENAME='||NM);
DBMS_OUTPUT.PUT_LINE('SAL='||SA);
DBMS_OUTPUT.PUT_LINE('TAX='||TAX);
DBMS_OUTPUT.PUT_LINE('NET='||NET);

EXCEPTION 
WHEN NO_DATA_FOUND
THEN DBMS_OUTPUT.PUT_LINE('INVALID EMPNO');
END;
/
