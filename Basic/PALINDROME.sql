SET SERVEROUTPUT ON

DECLARE

A NUMBER;
C NUMBER;
S NUMBER;
B NUMBER;
X EXCEPTION;

BEGIN
A:=&X;
S:=0;
B:=A;

IF(A<=0) THEN
RAISE X;
END IF;

LOOP
C:=MOD(B,10);
S:=S*10+C;
B:=FLOOR(B/10);
EXIT WHEN B=0;
END LOOP;

IF(S=A) THEN
DBMS_OUTPUT.PUT_LINE(A||' IS A PALINDROME');
ELSE
DBMS_OUTPUT.PUT_LINE(A||' IS NOT A PALINDROME');
END IF;

EXCEPTION
WHEN X THEN
DBMS_OUTPUT.PUT_LINE('INVALID INPUT');
END;
/



