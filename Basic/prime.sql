SET SERVEROUTPUT ON

DECLARE

A NUMBER;
FLAG NUMBER;
N NUMBER;
X EXCEPTION;

BEGIN
A:=&X;

IF(A<0) THEN
RAISE X;
END IF;

FLAG:=0;
N:=2;

WHILE N<=FLOOR(A/2)
LOOP

IF(MOD(A,N)=0) THEN
FLAG:=1;
EXIT;
END IF;

N:=N+1;
END LOOP;

IF(FLAG=0) THEN
DBMS_OUTPUT.PUT_LINE(A||' IS A PRIME NUMBER');
ELSE
DBMS_OUTPUT.PUT_LINE(A||' IS NOT A PRIME NUMBER');
END IF;

EXCEPTION
WHEN X THEN
DBMS_OUTPUT.PUT_LINE('INVALID INPUT');
END;
/



